import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IPaymentPlanItem } from '@/shared/model/payment-plan-item.model';

const baseApiUrl = 'api/payment-plan-items';

export default class PaymentPlanItemService {
  find(id: number): Promise<IPaymentPlanItem> {
    return new Promise<IPaymentPlanItem>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  create(entity: IPaymentPlanItem): Promise<IPaymentPlanItem> {
    return new Promise<IPaymentPlanItem>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  update(entity: IPaymentPlanItem): Promise<IPaymentPlanItem> {
    return new Promise<IPaymentPlanItem>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  partialUpdate(entity: IPaymentPlanItem): Promise<IPaymentPlanItem> {
    return new Promise<IPaymentPlanItem>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
